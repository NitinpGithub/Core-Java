package Constructor;

public class Constructor {

	/*
	 Constructor is a block of code similar to the method it is called when 
	 object/instance of a class is created
	 
	 it is a special type of method which used to initialize the object
	 
	 Constructor must have same name as a class name
	 Constructor does not return any value
	 Construction are called only at once at the time of object creation
	 Constructor can be overloaded
	 Constructor can be private - to restrict object creation
	 
	 Types - 
	 1) Default Constructor - When we create object of a class then automatically default constructor is created
	 2) Parameterized Constructor - When we pass any parameter while creating object then its called Parameterized
	 
	 */
}
